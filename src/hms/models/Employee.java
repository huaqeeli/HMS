package hms.models;


public class Employee {
    String militaryid , name, rank, idnamber, specialization, unitinforce;

    public String getMilitaryid() {
        return militaryid;
    }

    public void setMilitaryid(String militaryid) {
        this.militaryid = militaryid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRank() {
        return rank;
    }

    public void setRank(String rank) {
        this.rank = rank;
    }

    public String getIdnamber() {
        return idnamber;
    }

    public void setIdnamber(String idnamber) {
        this.idnamber = idnamber;
    }

    public String getSpecialization() {
        return specialization;
    }

    public void setSpecialization(String specialization) {
        this.specialization = specialization;
    }

    public String getUnitinforce() {
        return unitinforce;
    }

    public void setUnitinforce(String unitinforce) {
        this.unitinforce = unitinforce;
    }
    
}
